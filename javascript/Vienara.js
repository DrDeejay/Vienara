/**
 * Show the delete button
 * @since: 1.0 Beta 1
*/
function show_delete(div_id)
{
	// Make it visible
	$(div_id).show();
}

/**
 * This hides the delete button
 * @since: 1.0 Beta 1
*/
function hide_delete(div_id)
{
	// Make it visible
	$(div_id).hide();
}
